dna = ['AUG', 'AUC', 'UCG']

# Adds an item to the end of the list
dna.append('UAA')
print(dna)

# Removes an item from a list based on the value
dna.remove('AUC')
print(dna)

# Adds an item to a specific index
dna.insert(2, 'GAU')
print(dna)

# Removes the item at a particular index
dna.pop(0)
print(dna)
